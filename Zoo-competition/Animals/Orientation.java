package Animals;

/**
 * Represents the possible orientations for movement: East, South, West, and North.
 */
public enum Orientation {
    /**
     * The East direction.
     */
    EAST,

    /**
     * The South direction.
     */
    SOUTH,

    /**
     * The West direction.
     */
    WEST,

    /**
     * The North direction.
     */
    NORTH
}

